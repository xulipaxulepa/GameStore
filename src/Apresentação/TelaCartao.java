class ModeloDadosPedido extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return cartoes.size();
        }

        @Override
        public String getColumnName(int column) {
            if (column == 0) {
                return "Banco";
            } else {
                return "Numero Cartão";
            }
        }

        @Override
        public int getColumnCount() {
            return 2;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            CartãoDeCredito cartao = cartoes.get(rowIndex);
            if (columnIndex == 0) {
                return cartao.getBanco();
            } else {
                return cartao.getNumeroCartão();
            }
        }
    }

