    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaselecionada = this.tblCartoes.getSelectedRow();
        try {
            CartãoDeCredito cartao = this.cartoes.get(linhaselecionada);
            TelaCadastroCartao telacadastrocartao = new TelaCadastroCartao(this.telainicial, cartao);
            String mensagem = "Deseja alterar este cartão?";
            String titulo = "Alterar cartão";
            int resultado = JOptionPane.showConfirmDialog(this, mensagem, titulo, JOptionPane.YES_NO_CANCEL_OPTION);
            if (resultado == JOptionPane.YES_OPTION) {
                telacadastrocartao.setVisible(true);
                this.atualizaLista();
            } else {

            }
        } catch (ArrayIndexOutOfBoundsException e) {
            String mensagem = "Nenhum cartão selecionado";
            String titulo = "Erro";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int linhaselecionada = this.tblCartoes.getSelectedRow();
        try {
            this.dispose();
            String mensagem1 = "Compra bem sucedida";
            String titulo1 = "Sucesso";
            JOptionPane.showMessageDialog(this, mensagem1, titulo1, JOptionPane.INFORMATION_MESSAGE);
            this.telainicial.preencheTabelaUsuario();
        } catch (ArrayIndexOutOfBoundsException e) {
            String mensagem = "Nenhum cartão selecionado";
            String titulo = "Erro";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

    private void preencheTabela() {
        try {
            CartaoDeCreditoBO cartaodecreditoBO = new CartaoDeCreditoBO();
            this.cartoes = cartaodecreditoBO.buscarPorIDUsuario(this.telainicial.usuario);
            this.atualizaLista();
        } catch (SQLException sqle) {
            String mensagem = "Houve um erro na Exibiçao";
            String titulo = "Erro ao exibir pedido";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void atualizaLista() {
        ModeloDadosPedido modelo = new ModeloDadosPedido();
        this.tblCartoes.setModel(modelo);
    }
